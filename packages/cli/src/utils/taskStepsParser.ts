/**
 * Генерирует краткое напоминание для модели.
 */
export function generateReminder(): string {
  return `
💡 Напоминание:
• > 2 подзадач → todo_write
• Перед шагом: "▶ начинаю"
• После шага: обнови todo, выведи "✅ завершено"
• Инструменты → с правильными параметрами!
`;
}

/**
 * Генерирует подсказку при ошибке инструмента.
 */
export function generateToolErrorHint(toolName: string, error: string): string {
  const hints: Record<string, string> = {
    model_storage: `
❌ Ошибка model_storage: ${error}

💡 model_storage ТРЕБУЕТ:
   • operation (обязательно): set|get|search|delete|list|...
   • namespace (обязательно): knowledge|session|roadmap|...

✅ Пример:
{
  "operation": "set",
  "namespace": "knowledge",
  "key": "my_key",
  "value": {...}
}
`,
    todo_write: `
❌ Ошибка todo_write: ${error}

💡 todo_write требует:
{
  "todos": [
    {"id": "1", "content": "Задача", "status": "pending|in_progress|completed"}
  ]
}
`,
  };

  return (
    hints[toolName] || `❌ Ошибка ${toolName}: ${error}\n\nПроверь параметры!`
  );
}

/**
 * Улучшает промпт, добавляя правила поведения.
 * Главный entry point.
 */
export function enhancePromptWithRules(originalPrompt: string): string {
  const instructions = generateTaskInstructions();
  return `${instructions}

---

# Задание:

${originalPrompt}`;
}

